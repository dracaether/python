color=input("Type a color to determine if it's primary, secondary or tertiary: ")

if(color=="red") or (color=="blue") or (color=="yellow"):
    print("This is a primary color.")
elif(color=="green") or (color=="purple") or (color=="orange"):
    print("This is a secondary color.")
elif(color=="marigold") or (color=="vermilion") or (color=="teal") or (color=="lime"):
    print("This is a tertiary color.")
## this is optional
else:
    print("I don't recognize that color.")

## another optional thing
exit()
